public class UserDetailsViewId {

    public int id;
    public int applicationType;
    public int userName;
    public int email;
    public int password;
    public int number;
    public int description;
    public int securityKey;
    public int passwordCreatedDate;
    public int passwordExpiryDate;

    public void setId(int id) {
        this.id = id;
    }

    public void setApplicationType(int applicationType) {
        this.applicationType = applicationType;
    }

    public void setUserName(int userName) {
        this.userName = userName;
    }

    public void setEmail(int email) {
        this.email = email;
    }

    public void setPassword(int password) {
        this.password = password;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public void setDescription(int description) {
        this.description = description;
    }

    public void setSecurityKey(int securityKey) {
        this.securityKey = securityKey;
    }

    public void setPasswordCreatedDate(int passwordCreatedDate) {
        this.passwordCreatedDate = passwordCreatedDate;
    }

    public void setPasswordExpiryDate(int passwordExpiryDate) {
        this.passwordExpiryDate = passwordExpiryDate;
    }
}
